int	pkis_valid_type(int c)
{
	return ((c == 'c') || (c == 's') || (c == 'd') || (c == 'i') || (c == 'f')
		|| (c == 'u') || (c == 'x') || (c == 'X') || (c == 'p') || (c == '%'));
}

int	pkis_valid_flag(int c)
{
	return ((c == '-') || (c == ' ') || (c == '0') || (c == '.') || (c == '*')
		|| (c == '+') || (c == '#'));
}

int	pkmin(int a, int b)
{
	if (a > b)
		return (b);
	return (a);
}
